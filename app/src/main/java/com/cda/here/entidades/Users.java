package com.cda.here.entidades;

public class Users {
    private int idUs;

    private int idSes;
    private String nombreUs;
    private String pswUs;

    public int getIdUs() {
        return idUs;
    }

    public void setIdUs(int idUs) {
        this.idUs = idUs;
    }

    public String getNombreUs() {
        return nombreUs;
    }

    public void setNombreUs(String nombreUs) {
        this.nombreUs = nombreUs;
    }

    public String getPswUs() {
        return pswUs;
    }

    public void setPswUs(String pswUs) {
        this.pswUs = pswUs;
    }

    public int getIdSes() {
        return idSes;
    }

    public void setIdSes(int idSes) {
        this.idSes = idSes;
    }
}
